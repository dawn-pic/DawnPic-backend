package com.hanyujie.dawnpic.entity;


public class Image {
    public int id;
    public String name;

    public Image(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
